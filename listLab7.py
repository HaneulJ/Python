"""
다음과 같은 내용으로 구성되는 이차원 리스트를 생성한다.
    1행   10, 20, 30, 40, 50
    2행   5, 10, 15
    3행  11, 22, 33, 44
    4행  9, 8, 7, 6, 5, 4, 3, 2, 13

 행단위 합을 구하여 다음과 같이 출력한다.
    1행의 합은 x 입니다.
    2행의 합은 x 입니다.
    3행의 합은 x 입니다.
    4행의 합은 x 입니다.
"""

data = [[10,20,30,40,50],
        [5,10,15],
        [11,22,33,44],
        [9,8,7,6,5,4,3,2,13]]

sum1 = 0
for i in data[0]:
    sum1 += i
print("1행의 합은", sum1, "입니다.")

sum2 = 0
for i in data[1]:
    sum2 += i
print("2행의 합은", sum2, "입니다.")

sum3 = 0
for i in data[2]:
    sum3 += i
print("3행의 합은", sum3, "입니다.")

sum4 = 0
for i in data[3]:
    sum4 += i
print("4행의 합은", sum4, "입니다.")

# 한번에 만드는 식
for i in range(4):
    print(i, "행의 합은", sum(data[i]),"입니다.")